function buildRuntimeGroup(rawGroup) {
  const service = getServiceById(rawGroup.serviceId);
  const plan = getPlanById(rawGroup.serviceId, rawGroup.planId);

  if (!service || !plan) {
    throw new Error(`Invalid mock group mapping: ${rawGroup.id}`);
  }

  const totalSlots = plan.totalSeats ?? rawGroup.joinedSeats + rawGroup.availableSeats;
  const takenSlots = rawGroup.joinedSeats;

  if (
    plan.totalSeats != null &&
    rawGroup.joinedSeats + rawGroup.availableSeats !== plan.totalSeats
  ) {
    throw new Error(`Seat mismatch in mock group: ${rawGroup.id}`);
  }

  const calculatedSeatPrice = calculatePricePerSeat(rawGroup.serviceId, rawGroup.planId);
  const roundedSeatPrice =
    calculatedSeatPrice != null ? Math.round(calculatedSeatPrice) : null;
  const fallbackPlanPrice =
    roundedSeatPrice == null
      ? plan.price != null
        ? Math.round(plan.price)
        : rawGroup.price != null
          ? Math.round(rawGroup.price)
          : null
      : null;

  return {
    ...rawGroup,
    isApplied: rawGroup.isApplied ?? rawGroup.role === "member",
    isFollowed: Boolean(rawGroup.isFollowed),
    platform: service.name,
    plan: plan.name,
    host: rawGroup.hostName,
    totalSlots,
    takenSlots,
    pricePerSeat: roundedSeatPrice ?? fallbackPlanPrice,
    pricePerMonth: roundedSeatPrice ?? fallbackPlanPrice,
    currency: plan.currency,
    billingCycle: plan.billingCycle,
    officialPrice: plan.price,
    officialPriceLabel: plan.priceLabel,
  };
}

export const GROUPS_MOCK = CANONICAL_GROUPS.map(buildRuntimeGroup);
