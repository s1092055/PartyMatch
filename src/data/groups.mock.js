function slugifyHost(hostName) {
  return String(hostName)
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "") || "host";
}

function makeCanonicalGroup({
  id,
  serviceId,
  planId,
  title,
  hostName,
  joinedSeats,
  availableSeats,
  status = "open",
  tags = [],
  description,
  createdAt,
  nextPaymentDate,
  renewInDays,
  role = "none",
  price,
  joinPolicy = "review",
}) {
  return {
    id,
    serviceId,
    planId,
    title,
    hostName,
    ownerId: `mock-host-${slugifyHost(hostName)}`,
    joinedSeats,
    availableSeats,
    status,
    tags,
    description,
    createdAt,
    role,
    price,
    nextPaymentDate,
    renewInDays,
    joinPolicy,
  };
}

