export const serviceById = Object.fromEntries(
  SERVICES.map((service) => [service.id, service])
);

export function normalizeServiceId(input) {
  const value = String(input ?? "").trim().toLowerCase();
  if (!value) return "";

  for (const service of SERVICES) {
    if (service.id.toLowerCase() === value) return service.id;

    const aliasMatched = (service.aliases || []).some(
      (alias) => alias.toLowerCase() === value
    );

    if (aliasMatched) return service.id;
  }

  return "";
}

export function getServiceById(input) {
  const normalizedId = normalizeServiceId(input);
  return serviceById[normalizedId] ?? null;
}

export function getServiceCategoryById(categoryId) {
  return SERVICE_CATEGORIES.find((category) => category.id === categoryId) ?? null;
}

export function getServicesByCategory(categoryId) {
  return SERVICES.filter((service) => service.category === categoryId);
}

export function getServiceIconKey(input) {
  const service = getServiceById(input);
  return service?.brand ?? service?.id ?? normalizeServiceId(input);
}

export function getServiceIconPath(...inputs) {
  for (const input of inputs) {
    const iconPath = getServiceById(input)?.iconPath;
    if (iconPath) return iconPath;
  }

  return "/icons/google.png";
}

export function getPlanById(serviceId, planId) {
  const service = getServiceById(serviceId);
  if (!service) return null;
  return service.plans.find((plan) => plan.id === planId) ?? null;
}

export function calculatePricePerSeat(serviceId, planId) {
  const plan = getPlanById(serviceId, planId);
  if (!plan || !plan.price || !plan.totalSeats) return null;
  return plan.price / plan.totalSeats;
}
